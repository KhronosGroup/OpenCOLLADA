package de.netallied.xsd2cppsax;

/**
 * A primitive pair of strings.
 * 
 */
public class StringPair {

    public String str1;

    public String str2;

    public StringPair(String str1, String str2) {
        super();
        this.str1 = str1;
        this.str2 = str2;
    }
}
